let cost_1 = 100;
let cost_2 = 200;
let cost_3 = 500;

// using if-else statement
if (cost_1 > cost_2) {
  console.log("cost 1 is greater than cost 2");
} else {
  console.log("cost 2 is greater than cost 1");
}

// using ternary operator
cost_1 > cost_2
  ? console.log("cost 1 is greater than cost 2")
  : console.log("cost 2 is greater than cost 1");
